DATA G
CONST ZERO = 0
CONST ONE = 1
READ DX
MOV N,DX
MOV AX,ITER
MOV DX, N
MOV CX,ONE

X: IF AX GTEQ DX THEN GOTO Z
  MOV BX,ITER
  Y:IF BX GT AX THEN
       ADD AX,AX,CX
       JMP X
    MUL EX,AX,BX
    IF EX EQ DX THEN
       JMP P
    ELSE
       ADD BX,BX,CX
       JMP Y
Z:PRINT DX
  JMP Q
P: MOV CX,ZERO
PRINT CX
Q:END
